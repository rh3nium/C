// Relational Operators

#include <stdio.h>
int main() {
    printf("Relational Operators\n\n");
    printf("10>8 :%5d\n", 10>8); // Output: 10>8 : 1
    printf("10<5 :%5d\n", 10<5); // Output: 10<5 : 0
    printf("10!=10 :%5d\n", 10!=10); // Output: 10!=10 : 0
    printf("10==10 :%5d\n", 10==10); // 10==10 : 1
    printf("10>=10 :%5d\n", 10>=10); // Output: 10>=10 : 1
    printf("10<=30 :%5d\n", 10<=30); // Output: 10<=30 : 1
}