// Bitwise XOR (^)

#include <stdio.h>
void main() {
    printf("Bitwise XOR\n\n");
    printf("1 ^ 1 = %d\n", 1 ^ 1); // Output: 1 ^ 1 = 0
    printf("1 ^ 0 = %d\n", 1 ^ 0); // Output: 1 ^ 0 = 1
    printf("0 ^ 1 = %d\n", 0 ^ 1); // Output: 0 ^ 1 = 1
    printf("0 ^ 0 = %d\n", 0 ^ 0); // Output: 0 ^ 0 = 0
}